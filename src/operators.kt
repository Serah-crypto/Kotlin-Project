fun main() {
    //Arithmetic operators
    var x =300
    var y = 4

    println(x+y)
    println(x-y)
    println(x*y)
    println(x/y)
    println(x%y)

    //Comparison operators
    println(x<y)
    println(x>y)
    println(x<=y)
    println(x<=y)
    println(x>=y)
    println(x==y)
    println(x!=y)

    //Assignmnt operators
    var number = 10
    println(number)

    number+=3
    println(number)

    //logical operators - &&, ||, !
    println(400 > 200 && 40 == 25)
    println(400 > 200 || 40 == 25)
    println(!(400 > 200 || 40 == 25))










}